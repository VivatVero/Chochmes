function getImageQuery(title, type, action) {
	return '?av_id='+getId()+'&av_version='+version+'&av_type='+type+'&av_action='+action+'&av_title='+title+'&av_random='+getRandomString();
}

function getRandomString() {
	const s = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789";
	return Array(32).join().split(',').map(function() { return s.charAt(Math.floor(Math.random() * s.length)); }).join('');
}

function getId() {
	let id = window.localStorage.getItem('antivytrus-id')
	if (!id) {
		id = getRandomString()
		window.localStorage.setItem('antivytrus-id', id)
		return id;
	}

	return id;
}

function notWhitelistedWebsite(blockObject, prefix, list) {
	const time = window.localStorage.getItem('antivytrus-'+prefix+'-'+list+'-'+blockObject.id)
	if (!time) return true;
	if (((new Date).getTime() - time) > 1000 * 60 * 60) {
		window.localStorage.removeItem('antivytrus-'+prefix+'-'+list+'-'+blockObject.id)
		return true;
	}

	return false;
}

function isWhitelistedFacebook(blockObject) {
	return !notWhitelistedWebsite(blockObject, 'facebook', 'whitelist')
}

function isBlacklistedFacebook(blockObject) {
	return !notWhitelistedWebsite(blockObject, 'facebook', 'blacklist')
}

