function blockFacebook(blockData) {
	let wrappers = document.getElementsByClassName('userContentWrapper')

	if (wrappers.length === 0) {
		wrappers = document.getElementsByClassName('story_body_container')
	}

	for (let i = 0; i < wrappers.length; i++) {
		const wrapper = wrappers[i]

		if (isWrapperHandled(wrapper)) {
			continue;
		}

		const anchors = wrapper.getElementsByTagName('a')
		for (let j = 0; j < anchors.length; j++) {
			const link = cleanFBLink(anchors[j].href)
			const blockObject = getFacebookBlockObject(blockData, link)

			if (blockObject) {
				if (isBlacklistedFacebook(blockObject)) {
					hideFacebookWrapper(wrapper, blockObject)
					break;
				}

				if (!isWhitelistedFacebook(blockObject)) {
					blockFacebookWrapper(wrapper, blockObject)
					break;
				}
			}
		}
	}
}

function cleanFBLink(link) {
	const substring = 'https://l.facebook.com/l.php?u='
	if (link.includes(substring)) {
		return decodeURIComponent(link.substr(substring.length))
	}

	const mobileSubstring = 'https://lm.facebook.com/l.php?u='
	if (link.includes(mobileSubstring)) {
		return decodeURIComponent(link.substr(mobileSubstring.length))
	}
	return link;
}

