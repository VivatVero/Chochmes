function getBlockObject(blockData) {
	for (let i = 0; i < blockData.length; i++){
		const e = blockData[i]
		for (let j = 0; j < e.domains.length; j++) {
			let domain = e.domains[j]
			let numDots = domain.split('.').length-1
			let currentHost = window.location.hostname.split('.').slice(-1*numDots - 1).join('.')
			if (currentHost === domain) {
				return e;
			}
		}
	}
	
	return null;
}

function getFacebookBlockObject(blockData, link) {
	for (let i = 0; i < blockData.length; i++) {
		const blockObject = blockData[i];

		if (blockObject.facebook) {
			for (let j = 0; j < blockObject.facebook.length; j++) {
				if (link.includes(blockObject.facebook[j])) {
					return blockObject;
				}
			}
		}

		if (blockObject.domains) {
			for (let j = 0; j < blockObject.domains.length; j++) {
				matches = link.match(/^https?\:\/\/([^\/?#]+)(?:[\/?#]|$)/i);
				if (!matches || !matches[1]) {
					continue;
				}

				let domain = blockObject.domains[j]
				let numDots = domain.split('.').length-1
				let matchHost = matches[1].split('.').slice(-1*numDots - 1).join('.')

				if (domain === matchHost) {
					return blockObject;
				}
			}
		}	
	}


	return null;
}

